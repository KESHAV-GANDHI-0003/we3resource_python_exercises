'''Write a Python program to parse a string to float or integer.'''
float_ = input("Enter the float number :- ")
float_ = float(float_)
int_ = input("Enter the int number :- ")
int_ = int(int_)
print(f"Type of float number {float_} is {type(float_)}")
print(f"Type of int number {int_} is {type(int_)}")